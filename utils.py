def get_advice_for_score(key: str, score: int) -> str:
    title, _ = SIMPLE_QUESTIONS_MAP.get(key, ('Неизвестный параметр', ''))

    if key == 'stress':
        if score <= 4:
            analysis = "✅ **(1-4) Отлично!** Ваш уровень спокойствия в зеленой зоне."
            advice = "Совет: Продолжайте поддерживать свой баланс."
        elif 5 <= score <= 7:
            analysis = "⚠️ **(5-7) Умеренно.** Стресс требует внимания."
            advice = "Совет: Сделайте короткие перерывы в течение дня. Практикуйте дыхание 4-7-8."
        else:
            analysis = "❌ **(8-10) Критично!** Уровень стресса очень высокий."
            advice = "Совет: Примените технику заземления '5-4-3-2-1'. Если не улучшается, обратитесь к специалисту."
    else:
        if score <= 4:
            analysis = "❌ **(1-4) Критично!** Показатель в красной зоне."
            if key == 'sleep':
                advice = "Совет: Начните с гигиены сна: отложите гаджеты за час до сна, проветрите комнату."
            elif key in ('energy', 'motivation'):
                advice = "Совет: Используйте 'Правило 5 минут' для запуска деятельности."
            elif key == 'focus':
                advice = "Совет: Работайте по методу Помидоро."
            elif key == 'social':
                advice = "Совет: Напишите сообщение друзьям, запланируйте встречу."
            elif key == 'nutrition':
                advice = "Совет: Добавьте в рацион фрукты или овощи, пейте воду."
            else:
                advice = "Совет: Сфокусируйтесь на том, что в вашем контроле. Запишите три маленькие цели на сегодня."
        elif 5 <= score <= 7:
            analysis = "⚠️ **(5-7) Внимание.** Показатель удовлетворительный, есть потенциал для улучшения."
            if key == 'sleep':
                advice = "Совет: Ложитесь спать и вставайте в одно время."
            elif key in ('energy', 'motivation'):
                advice = "Совет: Короткая прогулка на свежем воздухе поднимет тонус."
            elif key == 'focus':
                advice = "Совет: Используйте список задач, выделите одну самую важную."
            elif key == 'social':
                advice = "Совет: Найдите время для общения с близкими."
