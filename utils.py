def simple_checkup_analysis(data):
    """
    data: словарь с ответами (ключи: stress, sleep, energy, focus, satisfaction,
          motivation, social, nutrition, hopes, control). Значения от 1 до 10.
    Возвращает общий результат и детальный разбор.
    """
    # Суммируем баллы
    score = sum([
        data.get('stress', 0),
        data.get('sleep', 0),
        data.get('energy', 0),
        data.get('focus', 0),
        data.get('satisfaction', 0),
        data.get('motivation', 0),
        data.get('social', 0),
        data.get('nutrition', 0),
        data.get('hopes', 0),
        data.get('control', 0)
    ])
    max_score = 100  # 10 параметров * 10 баллов максимум

    # Общая рекомендация
    if score >= 80:
        recommendation_title = "🌟 Отличное состояние"
        recommendation_text = "Вы демонстрируете высокий уровень психологического благополучия. Продолжайте в том же духе!"
    elif score >= 60:
        recommendation_title = "👍 Хороший уровень"
        recommendation_text = "У вас стабильное состояние, но есть зоны для улучшения. Обратите внимание на параметры с низкими оценками."
    elif score >= 40:
        recommendation_title = "⚠️ Средний уровень, требуется внимание"
        recommendation_text = "Ваше состояние находится в зоне риска. Рекомендуем поработать над слабыми сторонами (см. ниже)."
    else:
        recommendation_title = "❗ Низкий уровень – требуется помощь"
        recommendation_text = "Ваши ответы указывают на выраженные трудности. Не откладывайте заботу о себе. Обратитесь за поддержкой."

    # Детальный разбор
    details = []
    for param, label in SIMPLE_QUESTIONS_MAP.items():
        value = data.get(param, 0)
        comment = get_parameter_comment(param, value)
        details.append({
            'param': param,
            'label': label,
            'value': value,
            'max': 10,
            'comment': comment
        })

    return {
        'score': score,
        'max_score': max_score,
        'recommendation_title': recommendation_title,
        'recommendation_text': recommendation_text,
        'details': details
    }